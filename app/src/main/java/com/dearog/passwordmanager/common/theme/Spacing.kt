 * Standard spacing values used throughout the application.
 *
 * Avoid hardcoded dp values in UI. Use AppSpacing instead.
 */
object AppSpacing {

    /** 0dp */
    val None: Dp = 0.dp

    /** 2dp */
    val ExtraExtraSmall: Dp = 2.dp

    /** 4dp */
    val ExtraSmall: Dp = 4.dp

    /** 8dp */
    val Small: Dp = 8.dp

    /** 12dp */
    val MediumSmall: Dp = 12.dp

    /** 16dp */
    val Medium: Dp = 16.dp

    /** 20dp */
    val MediumLarge: Dp = 20.dp

    /** 24dp */
    val Large: Dp = 24.dp

    /** 32dp */
    val ExtraLarge: Dp = 32.dp

    /** 40dp */
    val ExtraExtraLarge: Dp = 40.dp

    /** 48dp */
    val Huge: Dp = 48.dp

    /** 56dp */
    val TouchTarget: Dp = 56.dp

    /** 64dp */
    val ScreenMargin: Dp = 64.dp
}